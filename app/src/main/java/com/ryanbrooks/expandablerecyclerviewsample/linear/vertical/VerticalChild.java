package com.ryanbrooks.expandablerecyclerviewsample.linear.vertical;

/**
 * Custom child list item.
 * <p/>
 * This is for demo purposes, although it is recommended having a separate
 * child list item from your parent list item.
 *
 * @author Gaurav B
 * @version 1.0
 * @since 12/30/2015
 */
public class VerticalChild {
    private String mDrNameText;
    private String mDrSpclText;
    private String mDrClassText;
    private String mDrTimeText;

    public String getmDrClassText() {
        return mDrClassText;
    }

    public void setmDrClassText(String mDrClassText) {
        this.mDrClassText = mDrClassText;
    }

    public String getmDrNameText() {
        return mDrNameText;
    }

    public void setmDrNameText(String mDrNameText) {
        this.mDrNameText = mDrNameText;
    }

    public String getmDrSpclText() {
        return mDrSpclText;
    }

    public void setmDrSpclText(String mDrSpclText) {
        this.mDrSpclText = mDrSpclText;
    }

    public String getmDrTimeText() {
        return mDrTimeText;
    }

    public void setmDrTimeText(String mDrTimeText) {
        this.mDrTimeText = mDrTimeText;
    }


}
